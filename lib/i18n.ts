// ─── t() helper ──────────────────────────────────────────────
// Haalt een geneste sleutel op via dot-notatie: t("profile.title")
export function getTranslations(locale: Locale) {
  const dict = translations[locale] ?? translations[DEFAULT_LOCALE];

  function t(key: string): string {
    const parts = key.split(".");
    let node: string | DeepRecord = dict;
    for (const part of parts) {
      if (typeof node !== "object") return key;
      node = (node as DeepRecord)[part];
      if (node === undefined) return key;
    }
    return typeof node === "string" ? node : key;
  }

  return { t, locale };
}

// ─── Cookie helpers ───────────────────────────────────────────
export const LOCALE_COOKIE = "nexsolve_locale";

export function getLocaleFromCookie(cookieHeader: string | null): Locale {
  if (!cookieHeader) return DEFAULT_LOCALE;
  const match = cookieHeader.match(new RegExp(`${LOCALE_COOKIE}=([^;]+)`));
  const val = match?.[1];
  if (val && ["en", "nl", "de", "fr"].includes(val)) return val as Locale;
  return DEFAULT_LOCALE;
}
